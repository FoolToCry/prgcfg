//  Description       : definition of some key codes
//----------------------------------------------------------------------------*/

#ifndef __keys_h__
#define __keys_h__

// control key overlapping with ascii alnum
#define K_POS1    49 /* Pos1     */
#define K_INS     50 /* Insert   */
#define K_DEL     51 /* Delete   */
#define K_END     52 /* End      */
#define K_PGUP    53 /* page up  */
#define K_PGDOWN  54 /* page down*/
#define K_UP      65 /* up       */
#define K_DOWN    66 /* down     */
#define K_RIGHT   67 /* right    */
#define K_LEFT    68 /* left     */

#define K_BCKSP  127 /* Backspace*/
#define K_CTRLH    8 /* Backspace, Ctrl H*/
#define K_ENTER   10 /* Enter    */
#define K_SOL      1 /* Ctrl a   */
#define K_D        4 /* Ctrl d   */
#define K_EOL      5 /* Ctrl e   */
#define K_CLREOL  11 /* Ctrl k   */
#define K_TAB      9 /* Tab      */
#define K_SPACE   32 /* Space    */
#define K_EOF     26 /* EndOfFile*/
#define K_ESC     27 /* Escape   */

// control key value < 0
#define C_POS1    -49 /* Pos1     */
#define C_INS     -50 /* Insert   */
#define C_DEL     -51 /* Delete   */
#define C_END     -52 /* End      */
#define C_PGUP    -53 /* page up  */
#define C_PGDOWN  -54 /* page down*/
#define C_UP      -65 /* up       */
#define C_DOWN    -66 /* down     */
#define C_RIGHT   -67 /* right    */
#define C_LEFT    -68 /* left     */

#define C_BCKSP  -127 /* Backspace*/
#define C_ENTER   -10 /* Enter    */
#define C_SOL      -1 /* Ctrl a   */
#define C_D        -4 /* Ctrl d   */
#define C_EOL      -5 /* Ctrl e   */
#define C_CLREOL  -11 /* Ctrl k   */
#define C_TAB      -9 /* Tab      */
#define C_SPACE   -32 /* Space    */
#define C_EOF     -26 /* EndOfFile*/
#define C_ESC     -27 /* Escape   */

#endif

